namespace TemplateMonster\ProductLabels\Controller\Adminhtml\Index;

use \Magento\Backend\App\Action;

class Index extends Action
{
    /**
     * @var PageFactory
     */
    protected $resultPageFactory;

    /**
     * @param Context $context
     * @param PageFactory $resultPageFactory
     */
    public function __construct(
        \Magento\Backend\App\Action\Context $context,
        \Magento\Framework\View\Result\PageFactory $resultPageFactory
    ) {
        parent::__construct($context);
        $this->resultPageFactory = $resultPageFactory;
    }
    /**
     * Check the permission to run it
     *
     * @return bool
     */
    protected function _isAllowed()
    {
        return $this->_authorization->isAllowed('TemplateMonster_ProductLabels::productlabels_grid');
    }

    /**
     * Index action
     *
     * @return \Magento\Backend\Model\View\Result\Page
     */
    public function execute()
    {
        /** @var \Magento\Backend\Model\View\Result\Page $resultPage */
        $resultPage = $this->resultPageFactory->create();
        $resultPage->setActiveMenu('TemplateMonster_ProductLabels::productlabels_grid');
        $resultPage->addBreadcrumb(__('Smart Product Labels'), __('Smart Product Labels'));
        $resultPage->addBreadcrumb(__('Smart Product Labels'), __('Smart Product Labels'));
        $resultPage->getConfig()->getTitle()->prepend(__('Smart Product Labels'));

        return $resultPage;
    }
}
